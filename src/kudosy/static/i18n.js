// ── Runtime state ─────────────────────────────────────────────────────────────

let _lang = 'de';

export function getLang() {
  const stored = localStorage.getItem('kudosy.lang');
  if (stored && SUPPORTED.includes(stored)) return stored;
  const nav = (navigator.language || '').slice(0, 2).toLowerCase();
  if (SUPPORTED.includes(nav)) return nav;
  return 'de';
}

export function localeFor(lang) {
  return LOCALES[lang || _lang] || 'de-DE';
}

export function currentLang() {
  return _lang;
}

export function t(key, params) {
  const msgs = MESSAGES[_lang] || MESSAGES['de'];
  const fallback = MESSAGES['de'];
  let str = msgs[key] ?? fallback[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replaceAll(`{${k}}`, String(v));
    }
  }
  return str;
}

export function applyStaticTranslations(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  root.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
}

export function setLang(lang, onChanged) {
  if (!SUPPORTED.includes(lang)) return;
  _lang = lang;
  localStorage.setItem('kudosy.lang', lang);
  document.documentElement.lang = localeFor(lang);
  applyStaticTranslations();
  if (onChanged) onChanged(lang);
}

